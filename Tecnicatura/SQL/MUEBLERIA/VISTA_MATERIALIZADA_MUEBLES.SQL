FROM DETALLE_MUEBLE AS DET 
JOIN TIPO_MUEBLES AS TMU ON TMU.TMU_ID = DET.DMU_TIPO_MUEBLE_ID
JOIN TIPO_MADERA AS TMA ON TMA.TMA_ID = DET.DMU_TIPO_MADERA_ID
JOIN NOMBRE_MUEBLE AS NM ON NM.NMU_ID = DET.DMU_NOMBRE_MUEBLE_ID
WHERE TMA.TMA_NAME = "SAUCE";

